import pandas as pd
import matplotlib.pyplot as plt

# יצירת נתונים לדוגמה
data = {
    'תאריך': ['2025-07-01', '2025-07-02', '2025-07-03', '2025-07-04', '2025-07-05', '2025-07-06', '2025-07-07'],
    'מספר שחרורים': [30, 45, 28, 40, 38, 50, 42]
}

# המרת הנתונים לטבלה
df = pd.DataFrame(data)

# המרת העמודה 'תאריך' לפורמט תאריך
df['תאריך'] = pd.to_datetime(df['תאריך'])

# יצירת גרף קווי
plt.figure(figsize=(10, 6))
plt.plot(df['תאריך'], df['מספר שחרורים'], marker='o', linestyle='-', color='green')

# כותרות וצירים
plt.title('מגמת שחרור מטופלים לפי תאריכים')
plt.xlabel('תאריך')
plt.ylabel('מספר שחרורים')

# סיבוב תוויות הציר האופקי (שמות התאריכים)
plt.xticks(rotation=45)

# רשת ברקע
plt.grid(True)

# הצגת הגרף
plt.tight_layout()
plt.show()


"""
מספר המטופלים הגבוה ביותר מוצג בגרף
"""
import pandas as pd

# הנתונים (כמו בדוגמה הקודמת)
data = {
    'מחלקה': ['פנימית', 'כירורגית', 'ילדים', 'יולדות', 'אורתופדית'],
    'מספר מטופלים': [120, 80, 95, 70, 60]
}

df = pd.DataFrame(data)

# מציאת המחלקה עם מספר המטופלים הגבוה ביותר
max_row = df.loc[df['מספר מטופלים'].idxmax()]
print(f"המחלקה עם הכי הרבה מטופלים היא: {max_row['מחלקה']} עם {max_row['מספר מטופלים']} מטופלים")



"""
ממוצע יומי של שחרור מטופלים בשבוע
"""
import pandas as pd

# הנתונים (כמו בדוגמה עם הגרף הקווי)
data = {
    'תאריך': ['2025-07-01', '2025-07-02', '2025-07-03', '2025-07-04', '2025-07-05', '2025-07-06', '2025-07-07'],
    'מספר שחרורים': [30, 45, 28, 40, 38, 50, 42]
}

df = pd.DataFrame(data)

# חישוב ממוצע
average_discharges = df['מספר שחרורים'].mean()
print(f"הממוצע היומי של שחרורי מטופלים הוא: {average_discharges:.2f}")
