
"""
מדריך בסיסי לספריית NumPy – מותאם לאנשי מערכת הבריאות .
NumPy היא ספרייה שמאפשרת לנו לעבוד עם נתונים בצורה נוחה ויעילה.
np.array: יוצר מבנה של נתונים דמוי טבלה.
np.mean, np.max: פונקציות לחישובים סטטיסטיים.
ניתן לבצע סינון, עדכון והשוואות על גבי המידע – הכל בצורה יעילה ופשוטה.
"""

import numpy as np

# דוגמה 1: יצירת מערך נתונים של טמפרטורות גוף של מטופלים
# נניח שיש לנו מדידות חום גוף של 5 מטופלים (בצלזיוס)
temperatures = np.array([36.7, 37.2, 38.1, 36.9, 39.0])
print("טמפרטורות הגוף:", temperatures)

# חישוב ממוצע הטמפרטורות
average_temp = np.mean(temperatures)
print("ממוצע הטמפרטורות:", average_temp)

# חישוב הטמפרטורה הגבוהה ביותר
max_temp = np.max(temperatures)
print("הטמפרטורה הגבוהה ביותר:", max_temp)

# דוגמה 2: השוואה – אילו מטופלים חורגים מטווח תקין?
# נניח שטווח תקין הוא עד 37.5
fever = temperatures > 37.5
print("למי יש חום?", fever)

# נוכל גם להדפיס את הערכים החריגים עצמם
print("ערכי חום חריגים:", temperatures[fever])

# דוגמה 3: עדכון נתונים
# נניח שהייתה טעות במדידה של המטופל השלישי (מיקום 2) – הערך האמיתי הוא 37.8
temperatures[2] = 37.8
print("הטמפרטורות לאחר עדכון:", temperatures)

# דוגמה 4: עבודה עם מטריצה (לוח של נתונים)
# נניח שיש לנו מדדים של לחץ דם (סיסטולי ודיאסטולי) ל-3 מטופלים
blood_pressure = np.array([
    [120, 80],
    [130, 85],
    [140, 90]
])
print("נתוני לחץ דם (שורה = מטופל):")
print(blood_pressure)

# חישוב ממוצע לחץ דם לכל מטופל
mean_bp_per_patient = np.mean(blood_pressure, axis=1)
print("ממוצע לחץ דם לכל מטופל:", mean_bp_per_patient)

# חישוב ממוצע לכל סוג מדד (סיסטולי ודיאסטולי)
mean_bp_per_type = np.mean(blood_pressure, axis=0)
print("ממוצע סיסטולי ודיאסטולי:", mean_bp_per_type)
